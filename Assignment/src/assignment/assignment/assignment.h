class Box {
public:

	unsigned int* diffuseTex;
	unsigned int* specularTex;

	glm::vec3 scale;
	glm::vec3 rotate;
	glm::vec3 translate;

	float angle;

	Box(
		unsigned int* diffuseTex = nullptr,
		unsigned int* specularTex = nullptr,
		float angle = 0.0f,
		glm::vec3 scale = glm::vec3(),
		glm::vec3 rotate = glm::vec3(),
		glm::vec3 translate = glm::vec3()
	) {
		this->diffuseTex = diffuseTex;
		this->specularTex = specularTex;

		this->angle = angle;
		this->scale = scale;
		this->rotate = rotate;
		this->translate = translate;
	}

	void render(Shader shader, unsigned int vao) {
		bind(vao);
		activateTextures();
		shader.setMat4("model", transform());
		draw();
	}

	void activateTextures() {
		glActiveTexture(GL_TEXTURE0);
		glBindTexture(GL_TEXTURE_2D, *diffuseTex);
		glActiveTexture(GL_TEXTURE1);
		glBindTexture(GL_TEXTURE_2D, *specularTex);
	}

	glm::mat4 transform() {
		glm::mat4 model = glm::mat4();

		model = glm::translate(model, translate);

		if (this->angle != 0.0f) {
			model = glm::rotate(model, glm::radians(angle), rotate);
		}

		model = glm::scale(model, scale);
		
		return model;
	}

	void bind(unsigned int vao) {
		glBindVertexArray(vao);
	}

	void draw() {
		glDrawArrays(GL_TRIANGLES, 0, 36);
	}

	glm::vec3 getNegativeBounds() {
		float negX = (-scale.x) / 2 + translate.x;
		float negY = (-scale.y) / 2 + translate.y;
		float negZ = (-scale.z) / 2 + translate.z;
		return glm::vec3(negX, negY, negZ);
	}
	
	glm::vec3 getPositiveBounds() {
		float posX = scale.x / 2 + translate.x;
		float posY = scale.y / 2 + translate.y;
		float posZ = scale.z / 2 + translate.z;
		return glm::vec3(posX, posY, posZ);
	}
};
